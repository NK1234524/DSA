public class Bit_operators{
    public static void main(String args[]){
        java.lang.System.out.println((5 & 6)); // AND operator 
        // 5 ki binary value
        // 6 ki binary value 
        // compare through & and then the 4 will be output (Binary Manipulation).

        System.out.println((5 | 6)); // OR operator
        System.out.println((5 ^ 6)); // XOR operator
        System.out.println((~5)); // NOT operator
        System.out.println((5<<2)); // Binary left shift operator
        System.out.println((6>>1)); // Binary left shift operator

        
    }    
}